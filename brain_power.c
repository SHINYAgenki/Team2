unsigned char gmul(unsigned char a, unsigned char b) {
    unsigned char p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        unsigned char hi_bit_set = a & 0x80;
        a <<= 1;
        if (hi_bit_set) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

void SubBytes(unsigned char *dst, unsigned char *src) {
    for (int i = 0; i < 16; i++) {
        dst[i] = g_sbox[src[i]];
    }
}

void ShiftRows(unsigned char *dst, unsigned char *src) {
    dst[0] = src[0]; dst[4] = src[4]; dst[8] = src[8]; dst[12] = src[12];
    unsigned char tmp = src[1];
    dst[1] = src[5]; dst[5] = src[9]; dst[9] = src[13]; dst[13] = tmp;
    tmp = src[2];
    dst[2] = src[10]; dst[10] = tmp; tmp = src[6];
    dst[6] = src[14]; dst[14] = tmp;
    tmp = src[3];
    dst[3] = src[15]; dst[15] = src[11]; dst[11] = src[7]; dst[7] = tmp;
}

void MixColumns(unsigned char *dst, unsigned char *src) {
    unsigned char tmp[16];
    for (int i = 0; i < 4; i++) {
        int idx = i * 4;
        tmp[idx] = gmul(src[idx],2)^gmul(src[idx+1],3)^gmul(src[idx+2],1)^gmul(src[idx+3],1);
        tmp[idx+1] = gmul(src[idx],1)^gmul(src[idx+1],2)^gmul(src[idx+2],3)^gmul(src[idx+3],1);
        tmp[idx+2] = gmul(src[idx],1)^gmul(src[idx+1],1)^gmul(src[idx+2],2)^gmul(src[idx+3],3);
        tmp[idx+3] = gmul(src[idx],3)^gmul(src[idx+1],1)^gmul(src[idx+2],1)^gmul(src[idx+3],2);
    }
    memcpy(dst, tmp, 16);
}

void AddRoundKey(unsigned char *dst, unsigned char *src, unsigned char *key) {
    for (int i = 0; i < 16; i++) dst[i] = src[i] ^ key[i];
}

typedef struct arg_struct {
    unsigned char *dst;
    unsigned char (*ptPairs)[2][16];
    int num;
    int type;
    int from;
    int to;
} arg_struct;

bool done;
pthread_mutex_t done_mutex = PTHREAD_MUTEX_INITIALIZER;

const int threadCount = 8;

void printAsHex(const char *label, unsigned char *key) {
    printf("%s: ", label);
    for (int i = 0; i < 16; i++) printf("%02x", key[i]);
    printf("\n");
}

void transform(unsigned char *state, unsigned char *rKey) {
    AddRoundKey(state, state, rKey);
    SubBytes(state, state);
    ShiftRows(state, state);
    MixColumns(state, state);
}

bool isOneByteOff(unsigned char *x, unsigned char *y, int type) {
    int diffCount = 0;
    for (int i = 0; i < 4; i++) {
        int index = 4 * type + i;
        if (x[index] != y[index]) diffCount++;
        if (diffCount > 1) return false;
    }
    return diffCount == 1;
}

void *findPartialKey(void *threadArg) {
    arg_struct *arg = (arg_struct *)threadArg;
    unsigned char state1[16], state2[16];
    unsigned char key[16] = {0};

    for (int k0 = arg->from; k0 < arg->to; k0++) {
        // if (done) return NULL;
        key[arg->type * 4] = k0;
        for (int k1 = 0; k1 < 256; k1++) {
            // key[arg->type * 4 + 1] = k1;
            for (int k2 = 0; k2 < 256; k2++) {
                // key[arg->type * 4 + 2] = k2;
                for (int k3 = 0; k3 < 256; k3++) {
                    // if (done) return NULL;
                    key[arg->type * 4 + 3] = k3;
                    for (int fileIndex = 0; fileIndex < arg->num; fileIndex++) {
                        memcpy(state1, arg->ptPairs[fileIndex][0], 16);
                        memcpy(state2, arg->ptPairs[fileIndex][1], 16);
                        transform(state1, key);
                        transform(state2, key);
                        if (!isOneByteOff(state1, state2, arg->type)) break;
                        if (fileIndex == arg->num - 1) {
                            pthread_mutex_lock(&done_mutex);
                            done = true;
                            pthread_mutex_unlock(&done_mutex);
                            memcpy(arg->dst, key, 16);
                            printAsHex("Partial key found", arg->dst);
                            return NULL;
                        }
                    }
                }
            }
        }
    }
    return NULL;
}

void combinePartialKeys(unsigned char *dst, unsigned char pKeys[4][16]) {
    for (int i = 0; i < 16; i++) {
        dst[i] = pKeys[0][i] | pKeys[1][i] | pKeys[2][i] | pKeys[3][i];
    }
}

