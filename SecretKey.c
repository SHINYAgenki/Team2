void AddRoundKey(unsigned char *dst, unsigned char *src, unsigned char *key) {
    for (int i = 0; i < 16; i++) {
        dst[i] = src[i] ^ key[i];
    }
}

void KeyExpansion(unsigned char *w, unsigned char *init_key) {
    unsigned char Rcon[16] = {
        0x01,0x01,0x02,0x04,0x08,0x10,0x20,0x40,
        0x80,0x1b,0x36,0x6c,0xd8,0xab,0x4d,0x9a,
    };
	unsigned char temp,temp1,temp2,temp3,temp4;
	const int Nb = 4;
	const int Nk = 4;
	const int Nr = 10;

    memcpy(w, init_key, 16);

	int i = 4*Nk;
    while(i < 4*Nb*(Nr+1)) {
        temp1 = w[i-4];
        temp2 = w[i-3];
        temp3 = w[i-2];
        temp4 = w[i-1];
	    if (i % (4*Nk) == 0){
            // RotWord and SubWord
            temp = temp1;
            temp1 = g_sbox[temp2];
            temp2 = g_sbox[temp3];
            temp3 = g_sbox[temp4];
            temp4 = g_sbox[temp];
            // Rcon
            temp1 = temp1 ^ Rcon[i/(4*Nk)];
            temp2 = temp2 ^ 0;
            temp3 = temp3 ^ 0;
            temp4 = temp4 ^ 0;
	    }
	    else if (Nk > 6 && (i%Nk) == 4) {
            //SubWord
            temp1 = g_sbox[temp1];
            temp2 = g_sbox[temp2];
            temp3 = g_sbox[temp3];
            temp4 = g_sbox[temp4];
		}
        w[i]   = w[i-4*Nk]   ^ temp1;
        w[i+1] = w[i-4*Nk+1] ^ temp2;
        w[i+2] = w[i-4*Nk+2] ^ temp3;
        w[i+3] = w[i-4*Nk+3] ^ temp4;
        i += 4;
    }
}

